# 함수
# 함수정의

def add(x,y):
    result = x + y
    return result

def sub(x,y):
    result = x - y
    return result

def mul(x,y):
    result = x * y
    return result

def div(x,y):
    result = x / y
    return result

def mod(x,y):
    result = x % y
    return result

# 함수호출
val = add(1024, 5)
print(val)

val = sub(1024, 1000)
print(val)

val = mul(512, 512)
print(val)

val = div(512, 2)
print(val)

val = mod(512, 5)
print(val)