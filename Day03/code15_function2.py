# 함수
# 함수정의

def add(x,y):
    result = x + y
    print(result)
    
def sub(x,y):
    result = x - y
    print(result)

def mul(x,y):
    result = x * y
    print(result)

def div(x,y):
    result = x / y
    print(result)

def mod(x,y):
    result = x % y
    print(result)

def hello():
    print("hello")
    return

def hello2():
    msg = "hello, hello"
    return msg
    
#함수호출
hello()
print(hello2())

